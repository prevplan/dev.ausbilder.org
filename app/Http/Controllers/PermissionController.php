    /**
     * Show the form for editing the specified resource.
     *
     * @param  User  $user
     * @return \Illuminate\Contracts\View\Factory|\Illuminate\View\View
     */
    public function edit(User $user)
    {
        abort_unless(Auth::user()->can('permissions.edit', session('company_id')), 403);
        abort_unless(in_array(session('company_id'), $user->companies->pluck('id')->toArray()), 403); // no trainer of actual company

        return view('permission.edit', compact('user'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  User  $user
     * @return \Illuminate\Http\RedirectResponse
     */
    public function update(Request $request, User $user)
    {
        abort_unless(Auth::user()->can('permissions.edit', session('company_id')), 403);
        abort_unless(in_array(session('company_id'), $user->companies->pluck('id')->toArray()), 403); // no trainer of actual company

        if ($request->permissions) {
            $user->syncPermissions($request->permissions, session('company_id'));
        } else { // has no permissions anymore
            $user->detachPermissions(Permission::all(), session('company_id'));
        }

        return redirect()->route('permission.edit', ['user' => $user->id])->with('status', __('permissions updated'));
    }

